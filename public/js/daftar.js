window.onload = function () {
    var provinceSel = document.getElementById("province");
    var citySel = document.getElementById("city");
    var subdistrictSel = document.getElementById("subdistrict");

    for (var x in subjectObject) {
        provinceSel.options[provinceSel.options.length] = new Option(x, x);
    }

    // untuk data list kabupaten / kota
    provinceSel.onchange = function () {
        // kosongkan list kota
        subdistrictSel.length = 1;
        citySel.length = 1;

        // list kota
        for (var y in subjectObject[this.value]) {
            citySel.options[citySel.options.length] = new Option(y, y);
        }
    };

    // Untuk list kecamatan
    citySel.onchange = function () {
        // dropdown kecamatan kosong
        subdistrictSel.length = 1;

        // list kecamatan
        var z = subjectObject[provinceSel.value][this.value];
        for (var i = 0; i < z.length; i++) {
            subdistrictSel.options[subdistrictSel.options.length] = new Option(
                z[i],
                z[i]
            );
        }
    };
};
